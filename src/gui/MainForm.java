    private void btnSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaleActionPerformed

        if (!validateInputs()) {
            lblStatusBar.setText("Validation failed");
            return;
        }

        try {
            int barcode = Integer.parseInt(txtBarcode.getText().trim());
            String name = txtProductName.getText().trim();
            String category = radItemBasedProduct.isSelected() ? "IBP" : "WBP";
            String manufacturer = txtManufacturer.getText().trim();
            int unitPrice = Integer.parseInt(txtUnitPrice.getText().trim());
            double amountPaid;
            Product product;

            if (radItemBasedProduct.isSelected()) {
                amountPaid = unitPrice;
                product = new ItemBasedProduct(barcode, name, category, manufacturer, unitPrice, amountPaid);
            } else {
                int weight = Integer.parseInt(txtWeight.getText().trim());
                amountPaid = (unitPrice * weight) / 1000.0;
                product = new WeightBasedProduct(barcode, name, category, manufacturer, weight, unitPrice, amountPaid);
            }

            ProductDA.addProduct(product);
            txtOutput.setText("Product added successfully.");
            lblStatusBar.setText("Product added successfully");
        } catch (Exception e) {
            txtOutput.setText("Error: " + e.getMessage());
            lblStatusBar.setText("Error during sale");
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnSaleActionPerformed

