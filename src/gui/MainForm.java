    private boolean validateInputs() {
        if (txtBarcode.getText().trim().isEmpty()
                || txtProductName.getText().trim().isEmpty()
                || txtUnitPrice.getText().trim().isEmpty()
                || (!radItemBasedProduct.isSelected() && !radWeightBasedProduct.isSelected())) {
            JOptionPane.showMessageDialog(this, "Please fill all required fields and select a product type.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (radWeightBasedProduct.isSelected() && txtWeight.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter weight for Weight-Based Product.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        try {
            Integer.parseInt(txtBarcode.getText().trim());
            Integer.parseInt(txtUnitPrice.getText().trim());
            if (radWeightBasedProduct.isSelected()) {
                Integer.parseInt(txtWeight.getText().trim());
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Barcode, Unit Price, and Weight must be numeric.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }

